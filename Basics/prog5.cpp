#include <iostream>

using namespace std;

int
main()
  {
  int x = 15, Y = 9;

  cout << "X + Y = " << x + Y << endl;
  cout << "X - Y = " << x - Y << endl;
  cout << "X * Y = " << x * Y << endl;
  cout << "X : Y = " << x / Y << endl;
  cout << "X %  Y = " << x % Y << endl;
  
  return(0);
  }
